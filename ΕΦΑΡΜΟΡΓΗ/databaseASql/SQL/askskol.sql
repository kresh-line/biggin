CREATE TABLE ΠΕΡΙΟΧΕΣ (
    ΚΠ         INT          NOT NULL,
    Δήμος      VARCHAR(100) NOT NULL,
    Νομός      VARCHAR(100) NOT NULL,
    Περιφέρεια VARCHAR(100) NOT NULL,
    CONSTRAINT PK_ΠΕΡΙΟΧΕΣ PRIMARY KEY (ΚΠ)
);

CREATE TABLE ΠΟΛΙΤΙΣΤΙΚΟΙ_ΟΡΓΑΝΙΣΜΟΙ (
    Όνομα        VARCHAR(200) NOT NULL,
    Είδος        VARCHAR(100) NOT NULL,
    Έτος_Ίδρυσης INT,
    Διεύθυνση    VARCHAR(200),
    URL          VARCHAR(300),
    Φωτογραφία   VARCHAR(300),
    Περιοχή      INT          NOT NULL,
    CONSTRAINT PK_ΠΟΛΙΤΙΣΤΙΚΟΙ_ΟΡΓΑΝΙΣΜΟΙ PRIMARY KEY (Όνομα),
    CONSTRAINT FK_ΟΡΓΑΝΙΣΜΟΙ_ΠΕΡΙΟΧΕΣ FOREIGN KEY (Περιοχή)
        REFERENCES ΠΕΡΙΟΧΕΣ(ΚΠ)
);

CREATE TABLE ΕΚΔΗΛΩΣΕΙΣ (
    Όνομα       VARCHAR(200) NOT NULL,
    Ημερομηνία  DATE         NOT NULL,
    Χώρος       VARCHAR(200),
    Περιγραφή   TEXT,
    Οργανισμός  VARCHAR(200) NOT NULL,
    CONSTRAINT PK_ΕΚΔΗΛΩΣΕΙΣ PRIMARY KEY (Όνομα, Ημερομηνία),
    CONSTRAINT FK_ΕΚΔΗΛΩΣΕΙΣ_ΟΡΓΑΝΙΣΜΟΙ FOREIGN KEY (Οργανισμός)
        REFERENCES ΠΟΛΙΤΙΣΤΙΚΟΙ_ΟΡΓΑΝΙΣΜΟΙ(Όνομα)
);

CREATE TABLE ΔΡΑΣΤΗΡΙΟΤΗΤΕΣ (
    Οργανισμός    VARCHAR(200) NOT NULL,
    Δραστηριότητα VARCHAR(200) NOT NULL,
    CONSTRAINT PK_ΔΡΑΣΤΗΡΙΟΤΗΤΕΣ PRIMARY KEY (Οργανισμός, Δραστηριότητα),
    CONSTRAINT FK_ΔΡΑΣΤΗΡΙΟΤΗΤΕΣ_ΟΡΓΑΝΙΣΜΟΙ FOREIGN KEY (Οργανισμός)
        REFERENCES ΠΟΛΙΤΙΣΤΙΚΟΙ_ΟΡΓΑΝΙΣΜΟΙ(Όνομα)
);

